package pkg36b.pkgfinal;

public class Items {
    private String name;
    private double price;
    
    public Items(String name, double price) {
        this.name = name;
        this.price = price;
    }
    
    public String getName() {
        return this.name;
    }
    
    public double getPrice() {
        return this.price;
    }
}

